package ru.itis.abstracts;

public class Rectangle extends AbstractFigure {
    private int a;
    private int b;

    public Rectangle(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    @Override
    public int area() {
        return a * b;
    }
}

